import axios from 'axios'
import { GetServerSidePropsContext } from 'next'
import Head from 'next/head'
import { ReactElement, useEffect } from 'react'
import { Layout } from '@/components/layout'
import { VideoList } from '@/modules/video-list'
import { NextPageWithLayout } from '@/pages/_app'
import useVideoStore from '@/store/video-store'
import { IVideoResponse } from '@/types/video'
import { BASE_URL } from '@/utils/constants'

const Home: NextPageWithLayout<IVideoResponse> = ({ videos, total }) => {
  const { setVideos } = useVideoStore()

  useEffect(() => {
    setVideos(videos)
  }, [videos, total])

  return (
    <>
      <Head>
        <title>Tak-Tak</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="h-full">
        <VideoList videos={videos} />
      </main>
    </>
  )
}

Home.getLayout = function getLayout(page: ReactElement) {
  return <Layout>{page}</Layout>
}

export async function getServerSideProps({ query }: GetServerSidePropsContext) {
  const { topic } = query
  let response

  if (topic) {
    console.log('topic', topic)
    const { data } = await axios.get<IVideoResponse>(`${BASE_URL}/api/discover/${topic}`)
    response = data
  } else {
    const { data } = await axios.get<IVideoResponse>(`${BASE_URL}/api/post`)
    response = data
  }

  return {
    props: {
      ...response,
    },
  }
}

export default Home
